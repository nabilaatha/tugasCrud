    private void table(){
     try{
            con = Koneksi.getKoneksi();
            String sql1 = "SELECT * FROM transaksi order by kode ASC";
            st = con.createStatement();
            rs = st.executeQuery(sql1);
            while(rs.next()){
                DefaultTableModel model = (DefaultTableModel) transaksi.getModel();
                Object[] row = new Object[8];
                row[0] = rs.getString("kode");
                row[1] = rs.getString("tgl");
                row[2] = rs.getString("judul");
                row[3] = rs.getString("harga");
                row[4] = rs.getString("jml_beli");
                row[5] = rs.getString("total");
                row[6] = rs.getString("jml_bayar");
                row[7] = rs.getString("kembalian");
                
                model.addRow(row);
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }

