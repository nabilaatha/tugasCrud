    private void transaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_transaksiMouseClicked
        int i = transaksi.getSelectedRow();
        TableModel model = transaksi.getModel();
        kode.setText(model.getValueAt(i, 0).toString());
        tgl.setText(model.getValueAt(i, 1).toString());
        judul.setSelectedItem(model.getValueAt(i, 2).toString());
        harga.setText(model.getValueAt(i, 3).toString());
        jml_beli.setValue(Integer.valueOf(model.getValueAt(i, 4).toString()));
        total.setText(model.getValueAt(i, 5).toString());
        jml_bayar.setText(model.getValueAt(i, 6).toString());
        kembalian.setText(model.getValueAt(i, 7).toString());
    }//GEN-LAST:event_transaksiMouseClicked

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try{
            con = Koneksi.getKoneksi();
            String sql = "UPDATE transaksi set tgl='"+tgl.getText()+"',judul='"+judul.getSelectedItem()+"',harga='"+harga.getText()+"',jml_beli='"+jml_beli.getValue()+"',total='"+total.getText()+"',jml_bayar='"+jml_bayar.getText()+"',kembalian='"+kembalian.getText()+"' where kode='"+kode.getText()+"'";
            st = con.createStatement();
            st.execute(sql);
            JOptionPane.showMessageDialog(null,"Berhasil Mengedit Data");
            DefaultTableModel model = (DefaultTableModel) transaksi.getModel();
            model.setRowCount(0);
            table();
            refresh();
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btnEditActionPerformed

